class Solution(object):
    def __init__(self):
        self.dir_x = [0, 1, 0, -1]
        self.dir_y = [1, 0, -1, 0]

    def solve(self, board):
        """
        :type board: List[List[str]]
        :rtype: None Do not return anything, modify board in-place instead.
        """
        if not board:return board

        m=len(board)
        n=len(board[0])

        #å¯¹è¾¹ç¼˜Oè¿›è¡Œdfs
        def dfs(x,y):
            if x<0 or x>=m or y<0 or y>=n or board[x][y]!='O':
                return
            board[x][y]='A'
            for i in range(4):
                dfs(x+self.dir_x[i],y+self.dir_y[i])

        for i in range(0,n):
            dfs(0,i)
            dfs(m-1,i)
        for i in range(0,m):
            dfs(i,0)
            dfs(i,n-1)

        for i in range(n):
            for j in range(m):
                if board[i][j] == "A":
                    board[i][j] = "O"
                elif board[i][j] == "O":
                    board[i][j] = "X"


        return board

