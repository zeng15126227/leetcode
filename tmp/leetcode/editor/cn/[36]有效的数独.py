class Solution(object):
    def isValidSudoku(self, board):
        """
        :type board: List[List[str]]
        :rtype: bool
        """
        #æ¯è¡Œä¸€ä¸ªæ•°å­—
        row = [[0] * 10 for _ in range(9)]
        #æ¯åˆ—ä¸€ä¸ªæ•°å­—
        col = [[0] * 10 for _ in range(9)]
        #æ¯ä¸ªæ ¼å­ä¸€ä¸ªæ•°å­—
        box = [[0] * 10 for _ in range(9)]

        for i in range(len(board)):
            for j in range(len(board[0])):
                if board[i][j]==".":
                    continue
                box_index = i//3*3 + j//3
                board_v = int(board[i][j])
                if row[i][board_v]:
                    return False
                if col[j][board_v]:
                    return False
                if box[box_index][board_v]:
                    return False

                row[i][board_v]=1
                col[j][board_v]=1
                box[box_index][board_v]=1

        return True




