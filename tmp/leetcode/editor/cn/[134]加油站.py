class Solution(object):
    def canCompleteCircuit(self, gas, cost):
        """
        :type gas: List[int]
        :type cost: List[int]
        :rtype: int
        """
        # æš´åŠ›è§£æ³•
        n = len(gas)
        for i in range(n):
            res = gas[i] - cost[i]
            index = (i + 1) % n
            while res > 0 and index != i:
                res += gas[index] - cost[index]
                index = (index + 1) % n
            if res >= 0 and index == i:
                return i
        return -1

